#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!f)
		return ;
	while (lst)
	{
		if (lst->content != NULL)
		{
			f(lst->content);
			lst = lst->next;
		}
	}
}
